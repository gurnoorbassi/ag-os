import process from "node:process";
import { buildAuditEventRecord } from "./audit-writer.mjs";
import { COST_LIMITS_USD } from "./cost-ledger-writer.mjs";
import { isoTimestamp, writeJson } from "./common.mjs";

const SAFE_FALSE_FLAGS = [
  "usesCredentials",
  "startsOauth",
  "connectsSocialAccounts",
  "postsContent",
  "schedulesContent",
  "usesAnalyticsApi",
  "activatesN8n",
  "triggersDeployment",
  "deploysProduction",
  "changesDomain",
  "changesDns",
  "usesPaidAction",
  "accessesCustomerData",
  "accessesProductionData",
  "changesLeadGen",
  "changesAiReceptionist",
  "changesConstitution"
];

const PROHIBITED_ACTION_LABELS = {
  usesCredentials: "credential use",
  startsOauth: "OAuth execution",
  connectsSocialAccounts: "social account connection",
  postsContent: "posting",
  schedulesContent: "scheduling",
  usesAnalyticsApi: "analytics API use",
  activatesN8n: "n8n activation",
  triggersDeployment: "deployment",
  deploysProduction: "production deployment",
  changesDomain: "domain change",
  changesDns: "DNS change",
  usesPaidAction: "paid action",
  accessesCustomerData: "customer data access",
  accessesProductionData: "production data access",
  changesLeadGen: "Lead Gen change",
  changesAiReceptionist: "AI Receptionist change",
  changesConstitution: "Constitution change"
};

function requiredString(errors, value, label) {
  if (typeof value !== "string" || value.trim().length === 0) {
    errors.push(`${label} is required`);
  }
}

function requiredArray(errors, value, label) {
  if (!Array.isArray(value) || value.length === 0) {
    errors.push(`${label} must not be empty`);
  }
}

function buildCostLedger({ input, timestamp }) {
  const estimatedUsd = Number(input.cost.estimatedUsd ?? 0);
  const actualUsd = Number(input.cost.actualUsd ?? 0);
  const approvalRequired = estimatedUsd > 0 || actualUsd > 0;
  const overLimit = estimatedUsd > COST_LIMITS_USD.perTaskMax || actualUsd > COST_LIMITS_USD.perTaskMax;

  return {
    costLedgerId: input.cost.costLedgerId,
    status: overLimit ? "blocked" : "active",
    budgetLimitsUsd: COST_LIMITS_USD,
    entries: [
      {
        costEntryId: `${input.cost.costLedgerId}-actual`,
        jobId: input.cost.jobId,
        projectId: input.projectId,
        costType: "actual",
        amountUsd: actualUsd,
        approvalRequired,
        ...(input.approval.approvalId ? { approvalId: input.approval.approvalId } : {}),
        status: overLimit ? "blocked" : "recorded"
      }
    ],
    summary: {
      estimatedTaskCostUsd: estimatedUsd,
      actualTaskCostUsd: actualUsd,
      dailyActualUsd: actualUsd,
      monthlyActualUsd: actualUsd,
      budgetStatus: overLimit ? "over_limit" : "within_limit"
    },
    safety: {
      createsBillingChange: false,
      usesPaidService: false,
      usesLiveApi: false,
      requiresOwnerApproval: approvalRequired
    },
    createdAt: timestamp,
    updatedAt: timestamp
  };
}

function buildConnectorRecord({ input, timestamp }) {
  return {
    connectorExecutionId: input.action.connectorExecutionId,
    status: input.action.status ?? "done",
    connectorId: input.action.connectorId,
    requestedAction: input.action.requestedAction,
    riskLevel: input.approval.riskLevel,
    projectId: input.projectId,
    approvalRequired: true,
    approvalId: input.approval.approvalId,
    requiredPermissions: input.action.requiredPermissions,
    evidenceRequired: input.action.evidenceRequired,
    safety: {
      executesLiveAction: input.action.executesLiveAction === true,
      usesCredentials: false,
      triggersDeployment: false,
      changesDomain: false,
      usesPaidAction: false,
      accessesProductionData: false
    },
    result: input.action.result ?? {},
    prohibitedActionsConfirmedFalse: input.action.prohibitedActionsConfirmedFalse,
    notes: input.action.notes ?? "Generated by Runtime Proof Writer v1 from explicit local input.",
    createdAt: timestamp,
    updatedAt: timestamp
  };
}

function buildApprovalRecord({ input, timestamp }) {
  return {
    ...input.approval,
    status: "approved",
    createdAt: input.approval.createdAt ?? timestamp,
    updatedAt: timestamp
  };
}

function buildDashboardSummary({ input, paths, timestamp }) {
  return {
    proofId: input.proofId,
    status: "recorded",
    title: input.summary.title,
    outcome: input.summary.outcome,
    projectId: input.projectId,
    approvalId: input.approval.approvalId,
    connectorExecutionId: input.action.connectorExecutionId,
    costLedgerId: input.cost.costLedgerId,
    references: input.references ?? {},
    sourceRecords: paths,
    safety: {
      localFilesOnly: true,
      liveActionsBlocked: true,
      credentialsStored: false,
      oauthExecuted: false,
      socialActionsExecuted: false,
      paidActionsUsed: false,
      productionDataAccessed: false
    },
    notes: input.summary.notes,
    generatedBy: "runtime-proof-writer-v1",
    createdAt: timestamp,
    updatedAt: timestamp
  };
}

export function validateRuntimeProofInput(input) {
  const errors = [];

  requiredString(errors, input?.proofId, "proofId");
  requiredString(errors, input?.projectId, "projectId");
  requiredString(errors, input?.approval?.approvalId, "approval.approvalId");
  requiredString(errors, input?.approval?.approvedBy, "approval.approvedBy");
  requiredString(errors, input?.approval?.commandCategory, "approval.commandCategory");
  requiredString(errors, input?.approval?.requestedAction, "approval.requestedAction");
  requiredString(errors, input?.approval?.target, "approval.target");
  requiredString(errors, input?.approval?.scope, "approval.scope");
  requiredString(errors, input?.approval?.riskLevel, "approval.riskLevel");
  requiredString(errors, input?.approval?.dataClass, "approval.dataClass");
  requiredString(errors, input?.approval?.revocationPath, "approval.revocationPath");
  requiredString(errors, input?.approval?.approvedAt, "approval.approvedAt");
  requiredString(errors, input?.approval?.expiresAt, "approval.expiresAt");
  requiredArray(errors, input?.approval?.approvalRequiredFor, "approval.approvalRequiredFor");
  requiredArray(errors, input?.approval?.approvedActions, "approval.approvedActions");
  requiredArray(errors, input?.approval?.prohibitedActions, "approval.prohibitedActions");
  requiredArray(errors, input?.approval?.evidence, "approval.evidence");
  requiredString(errors, input?.action?.connectorExecutionId, "action.connectorExecutionId");
  requiredString(errors, input?.action?.connectorId, "action.connectorId");
  requiredString(errors, input?.action?.requestedAction, "action.requestedAction");
  requiredArray(errors, input?.action?.requiredPermissions, "action.requiredPermissions");
  requiredArray(errors, input?.action?.evidenceRequired, "action.evidenceRequired");
  requiredArray(errors, input?.action?.prohibitedActionsConfirmedFalse, "action.prohibitedActionsConfirmedFalse");
  requiredString(errors, input?.cost?.costLedgerId, "cost.costLedgerId");
  requiredString(errors, input?.cost?.jobId, "cost.jobId");
  requiredString(errors, input?.summary?.title, "summary.title");
  requiredString(errors, input?.summary?.outcome, "summary.outcome");

  if (input?.approval?.status && input.approval.status !== "approved") {
    errors.push("approval.status must be approved when supplied");
  }

  for (const flag of SAFE_FALSE_FLAGS) {
    if (input?.safety?.[flag] !== false) {
      errors.push(`safety.${flag} must be false`);
    }
  }

  if (Number(input?.cost?.estimatedUsd ?? 0) > COST_LIMITS_USD.perTaskMax) {
    errors.push("estimated cost exceeds per-task limit");
  }

  if (Number(input?.cost?.actualUsd ?? 0) > COST_LIMITS_USD.perTaskMax) {
    errors.push("actual cost exceeds per-task limit");
  }

  return {
    valid: errors.length === 0,
    errors
  };
}

export function buildRuntimeProofRecords({ input, now = new Date() }) {
  const validation = validateRuntimeProofInput(input);
  if (!validation.valid) {
    const unsafeReasons = validation.errors
      .filter((error) => error.startsWith("safety."))
      .map((error) => error.replace(/^safety\./, "").replace(/ must be false$/, ""))
      .map((flag) => PROHIBITED_ACTION_LABELS[flag] ?? flag);
    const budgetReasons = validation.errors.filter((error) => error.includes("cost exceeds"));
    const prefix = unsafeReasons.length > 0
      ? `unsafe runtime proof input: ${unsafeReasons.join(", ")}`
      : budgetReasons[0] ?? `runtime proof input invalid: ${validation.errors.join("; ")}`;
    throw new Error(prefix);
  }

  const timestamp = isoTimestamp(now);
  const approval = buildApprovalRecord({ input, timestamp });
  const connector = buildConnectorRecord({ input, timestamp });
  const cost = buildCostLedger({ input, timestamp });
  if (cost.summary.budgetStatus === "over_limit") {
    throw new Error("actual cost exceeds per-task limit");
  }

  const paths = {
    approval: `.codex/approvals/${approval.approvalId}.json`,
    audit: `.codex/audit/audit-${input.proofId}-executed.json`,
    connector: `.codex/connectors/${connector.connectorExecutionId}.json`,
    cost: `.codex/costs/${cost.costLedgerId}.json`,
    dashboardSummary: `.codex/proofs/proof-${input.proofId}.json`
  };

  const audit = buildAuditEventRecord({
    runId: `${input.proofId}-executed`,
    actor: "ag-os-runtime",
    eventType: "validation_run",
    summary: `${input.summary.title}: ${input.summary.outcome}.`,
    scope: input.proofId,
    source: "local_validation",
    relatedArtifacts: [
      { type: "approval", reference: approval.approvalId },
      { type: "other", reference: connector.connectorExecutionId },
      { type: "other", reference: cost.costLedgerId },
      ...(input.references?.critiqueId ? [{ type: "other", reference: input.references.critiqueId }] : []),
      ...(input.references?.qualityScoreId ? [{ type: "other", reference: input.references.qualityScoreId }] : [])
    ],
    riskLevel: approval.riskLevel,
    dataClassification: approval.dataClass,
    liveServiceTouched: connector.safety.executesLiveAction,
    notes: "Generated by Runtime Proof Writer v1 from explicit input. This writer only creates local source-of-truth records and does not execute connector actions, credentials, OAuth, social actions, deployments, paid actions, production/customer data access, Lead Gen changes, AI Receptionist changes, or Constitution changes.",
    now
  });

  const dashboardSummary = buildDashboardSummary({ input, paths, timestamp });

  return {
    paths,
    approval,
    audit,
    connector,
    cost,
    dashboardSummary
  };
}

export function writeRuntimeProofRecords({ input, now, root = process.cwd() }) {
  const records = buildRuntimeProofRecords({ input, now });

  writeJson(records.paths.approval, records.approval, root);
  writeJson(records.paths.audit, records.audit, root);
  writeJson(records.paths.connector, records.connector, root);
  writeJson(records.paths.cost, records.cost, root);
  writeJson(records.paths.dashboardSummary, records.dashboardSummary, root);

  return records;
}
